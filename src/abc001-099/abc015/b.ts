export default {};

function solve() {
  const n = nextN();

  let count = 0;
  let total = 0;
  for (let i = 0; i < n; i++) {
    const a = nextN();
    if (0 < a) {
      count++;
      total += a;
    }
  }

  console.log(Math.ceil(total / count));
}

