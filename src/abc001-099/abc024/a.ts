export default {};

function solve() {
  const a = nextN();
  const b = nextN();
  const c = nextN();
  const k = nextN();
  const s = nextN();
  const t = nextN();

  let fee = a * s + b * t;
  if (k <= s + t) {
    fee -= c * (s + t);
  }

  console.log(fee);
}

