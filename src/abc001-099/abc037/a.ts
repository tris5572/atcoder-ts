function solve() {
  const a = nextN();
  const b = nextN();
  const c = nextN();
  let ans = 0;
  if (a <= b) {
    ans = Math.trunc(c / a);
  } else {
    ans = Math.trunc(c / b);
  }

  console.log(ans);
}

