export default {};

function solve() {
  const s = nextS();
  const a = [];

  for (const c of s.split("")) {
    if (c === "0") {
      a.push("0");
    } else if (c === "1") {
      a.push("1");
    } else {
      a.pop();
    }
  }

  console.log(a.join(""));
}

