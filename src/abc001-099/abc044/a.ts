export default {};

function solve() {
  const n = nextN();
  const k = nextN();
  const x = nextN();
  const y = nextN();
  let cost = 0;

  if (k < n) {
    cost += k * x;
    cost += (n - k) * y;
  } else {
    cost += n * x;
  }

  console.log(cost);
}

